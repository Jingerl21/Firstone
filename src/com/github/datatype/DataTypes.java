package com.github.datatype;

public class DataTypes {
	
	public static void main(String[] args) {
		
		int n = 1;
		System.out.println("Int value is " + n);
		
		boolean Ma = false;
		System.out.println("boolean value is " + Ma);
		
		float X = 0.01f;
		System.out.println("Float value is " + X);
		
		short M = -32500;
		System.out.println("Short Value is " + M);
		
		//Anas worked on byte, char, long and double
		
		byte b = 2;
		System.out.println("Byte value is " + b);
		
		char c = 'P';
		System.out.println("Char value is " + c);
		
		long l = 789123;
		System.out.println("Long value is " + l);
		
		double d = 0.0896;
		System.out.println("Double value is " + d);
	}

}
